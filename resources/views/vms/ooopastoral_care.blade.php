<h3 class="inr-hd-bg">Our Ethos</h3>
<p>The greatest strengths of the school spring from the quality of the   human relationships throughout the community and the sense of   companionship which supports the well being prospects for the boys. The full boarding environment   encourages a deep sense of community and helps to develop friendships   that will be sustained and, in turn, be sustaining in life beyond the   school. The seven Houses provide a superb system of pastoral care   promoting individual and academic confidence. Doscos come to appreciate   the creative tension between independence and interdependence and they   do so with dynamic effervescence and an obvious sense of fun.</p>
<p>The chance to live and work alongside people from vastly varied   backgrounds and states in India and from countries abroad is an   enriching experience. Boys work with teachers, other members of the   adult community and with other boys in ways that go beyond the   traditional relationship imposed by the classroom or laboratory.</p>
<p>The boarding houses at Doon provide the bedrock upon which supported   independence and youthful fellowship can be built. Boys learn to treat   each other and all members of the community with kindness, sensitivity   and respect.</p>
<p>Houses are staffed by teams of adults who work generously to sustain   and uphold the health and happiness of young people in their care. We   work in partnership with parents, based upon agreed values which support   the well-being of the boys. Housemasters are   supported by Assistant Housemasters, and a team of Dames and Tutors.   Each pupil meets regularly with his Tutor, who provides guidance on all matters related to the boys&#8217; development. Houses vary in character, composition and location but they   all share the central ethos of our community.</p>
<p>The working week at The Doon School is Monday to Friday and weekends offer a rich   programme of structured activities, social occasions and other   recreational opportunities.</p>
