<h3 class="inr-hd-bg">Social Service</h3>
<p><img class="alignleft" title="Social Service" src="../../wp-content/uploads/2018/02/Social-Service.jpg" alt="Social-service" width="230" /></p>
<p>Some social service projects are school-based and some others are undertaken by the boarding houses. Now, however, there is an increasing number of individual boys who are driving their own initiatives. For example, they have been involved in tackling substance abuse in slums and hill villages, and working with the police force. They have embarked on micro-finance projects to encourage the poor to start their own small-scale business and have initiated clean water production for domestic and business use. Creating a programme for cheaper brick production on a housing project is another of the many individual examples of social responsibility taken up by our boys. The school encourages the use of the school’s facilities by children from villages and slum areas. A key aim of all projects is the need to create sustainable models for improving the lives of the poor in urban slum areas and in hill villages. Boys themselves raise funds for these projects, rather than use their parents’ money. The boys deploy their knowledge of business theory learned in the classroom and apply it to regional projects making a real, long-term improvement to the lives of local people. The boys are the driving force in such projects, guided and supported by the faculty. The school assists financially with the operation and development of several slum and village schools. This is an ongoing and increasingly significant part of the curriculum at the Doon School. Many Old Boys continue to follow the school’s mission of social service and responsibility in their adult lives. Examples of projects of almost each class ranges from establishing NGOs and serving the poorer communities through medicine, dentistry and law through to establishing schools for the underprivileged sections of society to leading social corporate responsibility within their businesses.</p>
